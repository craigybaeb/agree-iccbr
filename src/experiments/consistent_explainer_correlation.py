class ConsistentExplainerMetrics:
    """
    Robustness metrics using CONSISTENT explainers (IG, DeepLift, LRP) across ALL metrics.
    """

    def __init__(self, model, k: int = 5, noise_level: float = 0.1, n_samples: int = 10):
        self.k = k
        self.noise_level = noise_level
        self.n_samples = n_samples
        self.model = model
        
        # Initialize all explainer methods
        self.explainer_methods = {
            'ig': IntegratedGradients(model),
            'dl': DeepLift(model), 
            'lrp': LRP(model)
        }

    @staticmethod
    def euclidean_distance(a: np.ndarray, b: np.ndarray) -> float:
        """Euclidean distance between two vectors."""
        return float(np.sqrt(np.sum((a - b) ** 2)))

    def cosine_similarity(self, a: np.ndarray, b: np.ndarray) -> float:
        """Compute cosine similarity between two vectors."""
        dot_product = np.dot(a.flatten(), b.flatten())
        norm_a = np.linalg.norm(a.flatten())
        norm_b = np.linalg.norm(b.flatten())
        return float(dot_product / (norm_a * norm_b + 1e-8))

    def captum_sensitivity_analysis(self, X: np.ndarray, explanations: np.ndarray, 
                                   index: int, method: str = 'ig') -> float:
        """
        Compute sensitivity using Captum's sensitivity_max with specified explainer method.
        Uses THE SAME explanation method as used for generating the input explanations.
        """
        try:
            x_i = X[index]
            
            # Convert to tensor
            input_tensor = torch.tensor(x_i, dtype=torch.float32).unsqueeze(0)
            input_tensor.requires_grad_(True)
            
            # Use specified explainer method (same as used for explanations)
            explainer = self.explainer_methods[method]
            
            # Use Captum's sensitivity_max metric
            sensitivity = sensitivity_max(
                explanation_func=explainer.attribute,
                inputs=input_tensor,
                perturb_radius=self.noise_level,
                n_perturb_samples=self.n_samples,
                target=0  # Binary classification
            )
            
            return float(sensitivity.item())
            
        except Exception as e:
            print(f"Captum sensitivity failed for sample {index} with {method}: {e}")
            # Fallback to explanation norm
            return float(np.linalg.norm(explanations[index]))

    def knn_similarity_robustness(self, X: np.ndarray, explanations: np.ndarray, 
                                 index: int) -> float:
        """
        K-nearest neighbor robustness using cosine similarity of explanations.
        Uses the SAME explanations generated by the specified explainer method.
        """
        x_i = X[index]
        expl_i = explanations[index]
        
        # Compute distances in input space to find k nearest neighbors
        distances = []
        for j in range(len(X)):
            if j != index:
                dist = self.euclidean_distance(x_i, X[j])
                distances.append((dist, j))
        
        # Sort by distance and take k nearest
        distances.sort(key=lambda x: x[0])
        k_nearest = distances[:min(self.k, len(distances))]
        
        if len(k_nearest) == 0:
            return 0.0
        
        # Compute cosine similarity in EXPLANATION space
        similarities = []
        for _, neighbor_idx in k_nearest:
            similarity = self.cosine_similarity(expl_i, explanations[neighbor_idx])
            similarities.append(similarity)
        
        return float(np.mean(similarities))


def simple_pearson_correlation(x: np.ndarray, y: np.ndarray) -> Tuple[float, int]:
    """Simple Pearson correlation coefficient."""
    # Remove NaN and infinite values
    mask = np.isfinite(x) & np.isfinite(y)
    if mask.sum() < 3:
        return 0.0, 0
    
    x_clean, y_clean = x[mask], y[mask]
    x_mean, y_mean = np.mean(x_clean), np.mean(y_clean)
    
    numerator = np.sum((x_clean - x_mean) * (y_clean - y_mean))
    x_var = np.sum((x_clean - x_mean) ** 2)
    y_var = np.sum((y_clean - y_mean) ** 2)
    denominator = np.sqrt(x_var * y_var)
    
    correlation = numerator / denominator if denominator > 0 else 0.0
    return float(correlation), int(mask.sum())


def load_data_simple(dataset: str, split: str = "test") -> Tuple[np.ndarray, np.ndarray]:
    """Simple data loader using basic file operations."""
    data_dir = ROOT / "data" / dataset
    
    # Try to load PyTorch tensors
    try:
        import torch
        X = torch.load(data_dir / f"X{split}.pt", map_location="cpu")
        y = torch.load(data_dir / f"y{split}.pt", map_location="cpu")
        
        if hasattr(X, "detach"):
            X = X.detach().cpu().numpy()
        if hasattr(y, "detach"):
            y = y.detach().cpu().numpy()
            
        return X.astype(np.float32), y.astype(int)
    except:
        print("PyTorch not available, trying NumPy files...")
        
    # Try NumPy files as fallback 
    try:
        X = np.load(data_dir / f"X{split}.npy")
        y = np.load(data_dir / f"y{split}.npy")
        return X.astype(np.float32), y.astype(int)
    except:
        print(f"Data files not found for dataset {dataset}, split {split}")
        raise FileNotFoundError(f"No data found at {data_dir}")


def run_consistent_explainer_experiment(
    dataset: str = "adult",
    split: str = "test", 
    n_samples: int = 50,
    noise_level: float = 0.1,
    k: int = 5,
    method: str = "ig",  # Explanation method: ig, dl, lrp
    seed: int = 42
) -> Dict:
    """
    Run correlation experiment with CONSISTENT explainers across all robustness metrics.
    This ensures fair comparison between Case Align and traditional robustness metrics.
    """
    
    print(f"🔬 Consistent Explainer Correlation Experiment")
    print(f"Dataset: {dataset}, Split: {split}, Samples: {n_samples}")
    print(f"Explainer: {method.upper()}, Noise: {noise_level}, K: {k}, Seed: {seed}")
    print("=" * 60)
    
    # Set random seed
    np.random.seed(seed)
    torch.manual_seed(seed)
    
    try:
        # Load data
        X, y = load_data_simple(dataset, split)
        print(f"✓ Loaded data: X shape {X.shape}, y shape {y.shape}")
        print(f"  Class distribution: {np.bincount(y)}")
        
        # Load trained model for the dataset
        print(f"📁 Loading trained model for {dataset}...")
        model = load_net(dataset)
        model.eval()
        print(f"✓ Model loaded successfully")
        
        # Generate explanations using specified method (IG/DL/LRP)
        print(f"🧠 Generating {method.upper()} explanations...")
        X_tensor = torch.tensor(X, dtype=torch.float32)
        explanations_dict = explain_batch(model, X_tensor, methods=[method], batch_size=64)
        explanations = explanations_dict[method].detach().cpu().numpy()
        print(f"✓ Explanations generated: shape {explanations.shape}")
        
    except Exception as e:
        print(f"❌ Failed to load data/model: {e}")
        print("🔄 Using synthetic data...")
        
        # Generate synthetic data as fallback
        X = np.random.randn(200, 20)
        y = (X[:, 0] + X[:, 1] > 0).astype(int)
        
        # Create simple model for synthetic data
        model = nn.Sequential(
            nn.Linear(20, 50),
            nn.ReLU(),
            nn.Linear(50, 1)
        )
        # Initialize properly
        for param in model.parameters():
            param.data.normal_(0, 0.1)
        model.eval()
        
        # Generate synthetic explanations
        X_tensor = torch.tensor(X, dtype=torch.float32)
        explanations_dict = explain_batch(model, X_tensor, methods=[method], batch_size=32)
        explanations = explanations_dict[method].detach().cpu().numpy()
        print(f"✓ Synthetic data: X {X.shape}, explanations {explanations.shape}")
    
    # Sample indices for evaluation
    n_eval = min(n_samples, X.shape[0])
    sample_indices = np.random.choice(X.shape[0], size=n_eval, replace=False)
    print(f"🎯 Evaluating {n_eval} samples")
    
    # Initialize case align with THE SAME explanations
    case_align = RobustnessCBR(
        k=k,
        m_unlike=1,
        sim_metric="gower",
        problem_metric="gower",
        like_only=True,  # Like-only variant
        robust_mode="geom",
        random_state=seed
    )
    
    case_align.fit(X, y, explanations)  # Using same explanations!
    print("✓ Case align fitted with consistent explanations")
    
    # Initialize consistent explainer metrics
    consistent_metrics = ConsistentExplainerMetrics(
        model=model, k=k, noise_level=noise_level, n_samples=10
    )
    
    # Compute ALL metrics using the SAME explanations
    print(f"🔄 Computing robustness metrics...")
    results = {
        'indices': [],
        'classes': [],
        'case_align_S_plus': [],
        'case_align_R_bounded': [],
        'captum_sensitivity': [],
        'knn_similarity_robustness': []
    }
    
    for i, sample_idx in enumerate(sample_indices):
        if (i + 1) % 10 == 0:
            print(f"  Progress: {i + 1}/{len(sample_indices)}")
        
        # Case align metrics (using same explanations)
        ca_result = case_align.compute_for_index(sample_idx)
        
        # Captum sensitivity (using same explainer method)
        sensitivity = consistent_metrics.captum_sensitivity_analysis(
            X, explanations, sample_idx, method=method
        )
        
        # k-NN similarity (using same explanations)
        knn_similarity = consistent_metrics.knn_similarity_robustness(
            X, explanations, sample_idx
        )
        
        # Store results
        results['indices'].append(sample_idx)
        results['classes'].append(y[sample_idx])
        results['case_align_S_plus'].append(ca_result.S_plus)
        results['case_align_R_bounded'].append(ca_result.R_bounded)
        results['captum_sensitivity'].append(sensitivity)
        results['knn_similarity_robustness'].append(knn_similarity)
    
    # Convert to numpy arrays
    for key in results:
        results[key] = np.array(results[key])
    
    print("✓ All metrics computed with consistent explanations")
    
    # Compute correlations
    print(f"\\n📊 Correlation Analysis ({method.upper()} explanations):")
    print("=" * 50)
    
    case_align_metrics = ['case_align_S_plus', 'case_align_R_bounded']
    traditional_metrics = ['captum_sensitivity', 'knn_similarity_robustness']
    
    correlations = []
    for ca_metric in case_align_metrics:
        for trad_metric in traditional_metrics:
            corr, n_valid = simple_pearson_correlation(
                results[ca_metric], 
                results[trad_metric]
            )
            
            print(f"{ca_metric} vs {trad_metric}:")
            print(f"  Correlation: {corr:.3f} (n={n_valid})") 
            
            correlations.append({
                'case_align': ca_metric,
                'traditional': trad_metric, 
                'correlation': corr,
                'n_valid': n_valid
            })
    
    # Summary statistics
    print(f"\\n📈 Summary Statistics:")
    print("=" * 30)
    for metric in case_align_metrics + traditional_metrics:
        values = results[metric]
        finite_values = values[np.isfinite(values)]
        if len(finite_values) > 0:
            print(f"{metric}:")
            print(f"  Mean: {np.mean(finite_values):.3f}")
            print(f"  Std:  {np.std(finite_values):.3f}")
            print(f"  Range: [{np.min(finite_values):.3f}, {np.max(finite_values):.3f}]")
        else:
            print(f"{metric}: No finite values")
    
    print(f"\\n🎯 Interpretation (using {method.upper()} explanations):")
    print("Expected relationships:")
    print("  - Case Align ↑ & Captum Sensitivity ↓ = Both indicate stability (NEGATIVE correlation)")
    print("  - Case Align ↑ & k-NN Similarity ↑ = Both indicate consistency (POSITIVE correlation)")
    
    return {
        'results': results,
        'correlations': correlations,
        'experiment_params': {
            'dataset': dataset,
            'split': split,
            'n_samples': n_eval,
            'noise_level': noise_level,
            'k': k,
            'method': method,
            'seed': seed
        }
    }


def main():
    parser = argparse.ArgumentParser(description="Consistent Explainer Correlation Experiment")
    parser.add_argument("--dataset", type=str, default="adult", help="Dataset name")
    parser.add_argument("--split", type=str, default="test", help="Data split")
    parser.add_argument("--n_samples", type=int, default=50, help="Number of samples")
    parser.add_argument("--noise_level", type=float, default=0.02, help="Noise level for sensitivity")
    parser.add_argument("--k", type=int, default=5, help="Number of neighbors")
    parser.add_argument("--method", type=str, default="ig", choices=["ig", "dl", "lrp"], 
                       help="Explanation method (ig=IntegratedGradients, dl=DeepLift, lrp=LRP)")
    parser.add_argument("--seed", type=int, default=42, help="Random seed")
    
    args = parser.parse_args()
    
    try:
        experiment_results = run_consistent_explainer_experiment(
            dataset=args.dataset,
            split=args.split,
            n_samples=args.n_samples,
            noise_level=args.noise_level,
            k=args.k,
            method=args.method,
            seed=args.seed
        )
        
        print(f"\\n🎉 Consistent Explainer Experiment completed successfully!")
        print(f"All metrics used {args.method.upper()} explanations for fair comparison")
        
        # Show strongest correlations
        correlations = experiment_results['correlations']
        if correlations:
            print(f"\\n🏆 Strongest correlations:")
            sorted_corr = sorted(correlations, key=lambda x: abs(x['correlation']), reverse=True)
            for corr in sorted_corr[:2]:
                sign = "+" if corr['correlation'] > 0 else ""
                print(f"  {corr['case_align']} vs {corr['traditional']}: {sign}{corr['correlation']:.3f}")
        
        return True
        
    except Exception as e:
        print(f"❌ Experiment failed: {e}")
        import traceback
        traceback.print_exc()
        return False


if __name__ == "__main__":
    main()